<?php
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "sistema_escolar";

// Crear conexión
$conn = new mysqli($servername, $username, $password, $dbname);

// Verificar conexión
if ($conn->connect_error) {
    die("<p class='error'>Error de conexión: " . $conn->connect_error . "</p>");
}

// Obtener los valores ingresados en el formulario
$numero_control = isset($_POST['numero_control']) ? $_POST['numero_control'] : '';
$nombre_completo = isset($_POST['nombre_completo']) ? $_POST['nombre_completo'] : '';

// Verificar que al menos uno de los campos esté lleno
if (!empty($numero_control) || !empty($nombre_completo)) {
    // Crear la consulta base
    $sql = "SELECT numero_control, nombre_completo, telefono_movil FROM datos_alumno WHERE 1=1";

    // Añadir condiciones según los campos llenos
    if (!empty($numero_control)) {
        $sql .= " AND numero_control = ?";
    }
    if (!empty($nombre_completo)) {
        $sql .= " AND nombre_completo LIKE ?";
    }

    $stmt = $conn->prepare($sql);

    // Crear los parámetros según las condiciones
    if (!empty($numero_control) && !empty($nombre_completo)) {
        $nombre_completo_param = "%$nombre_completo%";
        $stmt->bind_param("ss", $numero_control, $nombre_completo_param);
    } elseif (!empty($numero_control)) {
        $stmt->bind_param("s", $numero_control);
    } elseif (!empty($nombre_completo)) {
        $nombre_completo_param = "%$nombre_completo%";
        $stmt->bind_param("s", $nombre_completo_param);
    }

    $stmt->execute();
    $result = $stmt->get_result();

    if ($result->num_rows > 0) {
        // Imprimir los datos en una tabla HTML
        echo "<table>";
        echo "<tr><th>Número de Control</th><th>Nombre del Alumno</th><th>Teléfono Móvil</th><th>Acción</th></tr>";
        while ($row = $result->fetch_assoc()) {
            echo "<tr>";
            echo "<td>" . htmlspecialchars($row['numero_control']) . "</td>";
            echo "<td>" . htmlspecialchars($row['nombre_completo']) . "</td>";
            echo "<td>" . htmlspecialchars($row['telefono_movil']) . "</td>";
            echo "<td><a href='FICHA TÉCNICA DE IDENTIFICACIÓN.php?numero_control=" . urlencode($row['numero_control']) . "'>Saber más</a></td>";
            echo "</tr>";
        }
        echo "</table>";
    } else {
        echo "<p class='error'>No se encontraron resultados.</p>";
    }

    $stmt->close();
} else {
    echo "<p class='error'>Por favor, ingrese al menos un criterio de búsqueda.</p>";
}

$conn->close();
?>

